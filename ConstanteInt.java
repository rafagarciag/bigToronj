public class ConstanteInt{
	private int direccion;
	private int valor;

	public void setDireccion(int direccion){
		this.direccion=direccion;
	}

	public int getDireccion(){
		return direccion;
	}
	
	public void setValor(int valor){
		this.valor=valor;
	}
	
	public int getValor(){
		return valor;
	}
}
